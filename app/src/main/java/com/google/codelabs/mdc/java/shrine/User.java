package com.google.codelabs.mdc.java.shrine;

public class User {
    public String fname, lname, gender, birthday, email, serial;

    public User(){

    }


    public User(String fname, String lname, String gender, String birthday, String email, String serial) {
        this.fname = fname;
        this.lname = lname;
        this.gender = gender;
        this.birthday = birthday;
        this.email = email;
        this.serial = serial;
    }


}
